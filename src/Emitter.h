#pragma once

#include "Sprite.h"


//  General purpose Emitter class for emitting sprites
//  This works similar to a Particle emitter
//
class Emitter : public BaseObject {
public:
	Emitter(SpriteSystem *);
	void draw();
	void start();
	void stop();
	void setLifespan(float);
	void setVelocity(ofVec3f);
	void setChildImage(ofImage);
	void setChildSoundPlayer(ofSoundPlayer);
	void setChildSize(float w, float h) { childWidth = w; childHeight = h; }
	void setImage(ofImage);
	void setRate(float);
	float maxDistPerFrame();
	void update();
	SpriteSystem *sys;
	float rate;
	ofVec3f velocity;
	float lifespan;
	bool started;
	float lastSpawned;
	ofSoundPlayer childSoundPlayer;
	ofImage childImage;
	ofImage image;
	bool drawable;
	bool haveChildSoundPlayer;
	bool haveChildImage;
	bool haveImage;
	float width, height;
	float childWidth, childHeight;
	int score;
};
